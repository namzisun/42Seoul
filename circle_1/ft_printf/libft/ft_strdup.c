#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*copy;
	int		len;
	int		i;

	i = 0;
	len = ft_strlen(s);
	copy = (char *)malloc(sizeof(char) * (len + 1));
	while (s[i] != '\0')
	{
		copy[i] = s[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
